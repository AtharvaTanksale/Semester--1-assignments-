//C program to illustrate relational operators
#include<stdio.h>
int main(){
    int a=20, b=4;
    // using operators and printing results
    // 1 is true and 0 is false
    printf("a < b : %d\n", a < b);
    //prints the value 0 
    printf("a > b: %d\n", a > b );
    //prints the value 1 
    printf("a <= b :%d\n", a <= b);
    //prints the value 0
    printf("a >= b :%d\n", a >= b);
    //prints the value 1
    printf("a == b :%d\n", a == b);
    //prints the value 0
    printf("a != b :%d\n",a != b);
    //prints the value 1
    return 0;

}
