//C program to illustrate Arithmetic operators
#include<stdio.h>
int main()
{
    int a=115, b=23;
    printf("a+b=%d\n", a+b);
    //prints the value of a+b after addition
    printf("a-b=%d\n",a-b);
    //prints the value of a-b after subtraction
    printf("a*b=%d\n",a*b);
    //prints the valuye of a*b after multiplication
    printf("a/b=%d\n", a/b);
    //prints the value of a/b after dividing
    printf("a %% b=%d\n", a%b);
    //prints the value of a%b after taking mod
    printf("+a =%d\n",+a);
    //prints the value of a positively
    printf("-a=%d\n", -a);
    //prints the value of a negatively
    printf("a++ =%d\n",++a);
    //prints the value of a after increament
    printf("a-- =%d\n",--a);
    //prints the vlaue of a after decreament 
    return 0;
    
}